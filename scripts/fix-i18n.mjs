// Apply translations to all language files
const files = ["en-US", "zh-TW", "ja", "ko", "fr", "de", "es", "ru", "hi", "ar"];

for (const lang of files) {
  const path = `src/i18n/locales/${lang}.json`;
  const data = JSON.parse(readFileSync(path, "utf-8"));
  const trans = translations[lang];

  for (const [key, value] of Object.entries(trans)) {
    const parts = key.split(".");
    let obj = data;
    for (let i = 0; i < parts.length - 1; i++) {
      if (!obj[parts[i]]) { obj[parts[i]] = {}; }
      obj = obj[parts[i]];
    }
    obj[parts[parts.length - 1]] = value;
  }

  writeFileSync(path, JSON.stringify(data, null, 2) + "\n", "utf-8");
  console.log(`${lang}: ${Object.keys(trans).length} keys added`);
}
console.log("All 10 language files updated.");
